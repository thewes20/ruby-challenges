 weather_types = "sunny"
 
 case weather_types
 when "sunny" 
 	puts "Grab your bathing suit and hit the beach!"
 when "rainy"
 	puts "Grab your umbrella and rain boots!"
 when "windy"
 	puts "Don't forget a hat to keep your hair out of your face!"
 when "snowy"
 	puts "Wear your warmest jacket and don't forget your scarf and gloves!"
 when "cloudy"
 	puts "A light jacket will keep the slight chill away."
 else
 	puts "I haven't the slighest idea what you should wear!"
 end